public class user_data
{
    public String id;
    public int x;
    public int y;
    public double rotation;
    public int counter;

    public user_data(String id, int x, int y, double rotation)
    {
        this.id = id;
        this.x = x;
        this.y = y;
        this.rotation = rotation;
        this.counter = 0;
    }
}